public class _q4_inbuildMethods {
    public static void main(String[] args) {
        System.out.println(Math.pow(10,4));
        System.out.println(Math.sqrt(64));
        System.out.println(Math.min(12, 11));
        System.out.println(Math.max(12, 11));
        System.out.println(Math.abs(-23));
        System.out.println(Math.ceil(-23.3));
        System.out.println(Math.floor(-23.3));
        System.out.println(Math.cos(90));
        System.out.println(Math.nextUp(5.5));
        System.out.println(Math.round(34.434));
    }
}
